class Products {
  constructor(id, name,category, quantity, unitprice,totalvalue,location,status,action, expiration) {
    this.id = id;
    this.name = name;
    this.category = category;
    this.quantity = quantity;
    this.unitprice = unitprice;
    this.totalvalue = totalvalue;
    this.location = location;
    this.status = status;
    this.action = action;
    this.expiration = expiration;
  }


  
}

export default Products;
